public class LifeInfoBean {

    /**
     * co2 : 3777
     * light : 1777
     * pm2 : 109
     * temperature : 20
     */

    private int co2;
    private int light;
    private int pm2;
    private int temperature;

    public LifeInfoBean(int co2, int light, int pm2, int temperature) {
        this.co2 = co2;
        this.light = light;
        this.pm2 = pm2;
        this.temperature = temperature;
    }

    public int getCo2() {
        return co2;
    }

    public void setCo2(int co2) {
        this.co2 = co2;
    }

    public int getLight() {
        return light;
    }

    public void setLight(int light) {
        this.light = light;
    }

    public int getPm2() {
        return pm2;
    }

    public void setPm2(int pm2) {
        this.pm2 = pm2;
    }

    public int getTemperature() {
        return temperature;
    }

    public void setTemperature(int temperature) {
        this.temperature = temperature;
    }

    @Override
    public String toString() {
        return "LifeInfoBean{" +
                "co2=" + co2 +
                ", light=" + light +
                ", pm2=" + pm2 +
                ", temperature=" + temperature +
                '}';
    }
}
